const mongoose = require("mongoose")

const creatorSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
})

const Creator = mongoose.model("Creator", creatorSchema)
module.exports = Creator